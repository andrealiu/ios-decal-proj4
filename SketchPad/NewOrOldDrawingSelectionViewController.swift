    override func prepareForSegue(segue: UIStoryboardSegue, sender: AnyObject?) {
        let canvasViewController = segue.destinationViewController as! CanvasViewController
        if segue.identifier == "resumeDrawingSegue" {
            canvasViewController.resumeDrawing = true
        } else  {
            //let blankCanvas = UIImage()
            canvasViewController.resumeDrawing = false
        }
    }
    
    func loadImageFromPath(path: String) -> UIImage? {
        
        let image = UIImage(contentsOfFile: path)
        
        if image == nil {
            
            print("missing image at: \(path)")
        }
        print("Loading image from path: \(path)") // this is just for you to see the path in case you want to go to the directory, using Finder.
        return image
        
    }
    
    func getDocumentsURL() -> NSURL {
        let documentsURL = NSFileManager.defaultManager().URLsForDirectory(.DocumentDirectory, inDomains: .UserDomainMask)[0]
        return documentsURL
    }
    
    func fileInDocumentsDirectory(filename: String) -> String {
        
        let fileURL = getDocumentsURL().URLByAppendingPathComponent(filename)
        return fileURL.path!
        
    }
    

